public class Swicth{
    public static void main (String[] args){
        Integer value = 100;
        switch (value){
            case 10:
            System.out.println("Nilai sama dengan 10");
            break;
            case 20:
            System.out.println("Nilai sama dengan 20");
            break;
            default : System.out.println("Nilai tidak ada yang sama ");
        }
        
    }
}